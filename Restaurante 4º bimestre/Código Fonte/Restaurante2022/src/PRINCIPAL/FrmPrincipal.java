    private void logoff() {
        limpar();
        registrarLogout();
        cpfAutor = null;
        lblAviso.setText("Usu√°rio deslogado.");
        fun.setLogado(false);
        mnuCadastro.setEnabled(false);
        mnuReserva.setEnabled(false);
        mnuAdmin.setEnabled(false);
        btnLogin.setEnabled(true);
    }

    private void loginSN() {
        String cpf = txtCpf.getText();
        String senha = new String(txtSenha.getPassword());
        cpfAutor = cpf;
        if (cif.pesquisarCpf(cpf) && cif.pesquisarSenha(senha,cpf)) {
            registrarLogin();
            JOptionPane.showMessageDialog(null, "Voce esta logado como: " + cpf, "LOGIN", JOptionPane.INFORMATION_MESSAGE);
            if (cif.pesquisarNivel(cpf).matches("0")) {
                mnuReserva.setEnabled(true);
            } else if (cif.pesquisarNivel(cpf).matches("1")) {
                mnuCadastro.setEnabled(true);
                mnuReserva.setEnabled(true);
            } else if (cif.pesquisarNivel(cpf).matches("2")) {
                mnuCadastro.setEnabled(true);
                mnuReserva.setEnabled(true);
                mnuAdmin.setEnabled(true);
            }
            limpar();
            fun.setLogado(true);
            btnLogin.setEnabled(false);

        } else {
            if (fun.getLogado() == false) {
                JOptionPane.showMessageDialog(null, "CPF ou Senha incorretos.\n"
                        + "Por favor tente novamente e insira os valores corretos.");
                r.setAcaoRegistro("Tentativa de login fracassada.");
                registrarTLogin();
            }
        }
    }

    private void limpar() {
        lblAviso.setText("");
        txtCpf.setText(null);
        txtSenha.setText(null);
    }
    
    private void preencherRegistro() {
        r.setCpfAutor(cpfAutor);
        r.setData(dh.getData());
        r.setHora(dh.getHora());
    }
    
    private void registrarInicio() {
        r.setAcaoRegistro("Sistema iniciado.");
        preencherRegistro();
        reg.registrar(r, dh);
    }
    private void registrarFim() {
        r.setAcaoRegistro("Sistema finalizado.");
        preencherRegistro();
        reg.registrar(r, dh);
    }

    private void registrarLogin() {
        r.setAcaoRegistro("Tentativa de login bem-sucedida.");
        preencherRegistro();
        reg.registrar(r, dh);
    }

    private void registrarTLogin() {
        r.setAcaoRegistro("Tentativa de login fracassada.");
        preencherRegistro();
        reg.registrar(r, dh);
    }

    private void registrarLogout() {
        r.setAcaoRegistro("Usu√°rio efetuou o logout.");
        preencherRegistro();
        reg.registrar(r, dh);
    }

